CREATE PROCEDURE dbo.SP_GET_SINHVIEN_THEO_LOP
    @MALOP NCHAR(15)
AS
BEGIN
    SET NOCOUNT ON;

    SELECT
        MASV = RTRIM(MASV),
        HO = LTRIM(RTRIM(ISNULL(HO, N''))),
        TEN = LTRIM(RTRIM(ISNULL(TEN, N''))),
        NGAYSINH,
        DIACHI = LTRIM(RTRIM(ISNULL(DIACHI, N''))),
        MALOP = RTRIM(MALOP)
    FROM dbo.SINHVIEN
    WHERE RTRIM(MALOP) = RTRIM(@MALOP)
      AND TRANGTHAI = 1
    ORDER BY TEN, HO, MASV;
END
GO

