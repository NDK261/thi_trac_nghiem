CREATE PROCEDURE dbo.SP_GET_GIAOVIEN
AS
BEGIN
    SET NOCOUNT ON;

    SELECT
        MAGV = RTRIM(MAGV),
        HO = LTRIM(RTRIM(ISNULL(HO, N''))),
        TEN = LTRIM(RTRIM(ISNULL(TEN, N''))),
        SODTLL = LTRIM(RTRIM(ISNULL(SODTLL, N''))),
        DIACHI = LTRIM(RTRIM(ISNULL(DIACHI, N''))),
        HOTEN = LTRIM(RTRIM(ISNULL(HO, N''))) + N' ' + LTRIM(RTRIM(ISNULL(TEN, N'')))
    FROM dbo.GIAOVIEN
    WHERE TRANGTHAI = 1
    ORDER BY TEN, HO, MAGV;
END
