CREATE PROCEDURE dbo.SP_THEM_GIAOVIEN
    @MAGV NVARCHAR(50),
    @HO NVARCHAR(100),
    @TEN NVARCHAR(100),
    @SODTLL NVARCHAR(50) = NULL,
    @DIACHI NVARCHAR(200) = NULL
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @MAGV_TXT NVARCHAR(50), @HO_TXT NVARCHAR(100), @TEN_TXT NVARCHAR(100);
    DECLARE @SODTLL_TXT NVARCHAR(50), @DIACHI_TXT NVARCHAR(200);
    DECLARE @MAGV_CHUAN NCHAR(8), @SODTLL_CHUAN NCHAR(15);

    SET @MAGV_TXT = UPPER(LTRIM(RTRIM(ISNULL(@MAGV, N''))));
    SET @HO_TXT = LTRIM(RTRIM(ISNULL(@HO, N'')));
    SET @TEN_TXT = LTRIM(RTRIM(ISNULL(@TEN, N'')));
    SET @SODTLL_TXT = LTRIM(RTRIM(ISNULL(@SODTLL, N'')));
    SET @DIACHI_TXT = LTRIM(RTRIM(ISNULL(@DIACHI, N'')));

    IF @MAGV_TXT = N'' OR @HO_TXT = N'' OR @TEN_TXT = N''
       OR LEN(@MAGV_TXT) > 8 OR LEN(@HO_TXT) > 40 OR LEN(@TEN_TXT) > 10
       OR LEN(@SODTLL_TXT) > 15 OR LEN(@DIACHI_TXT) > 50
       OR @MAGV_TXT COLLATE Latin1_General_BIN2 LIKE N'%[^A-Z0-9]%'
       OR @HO_TXT LIKE N'%[0-9]%' OR @TEN_TXT LIKE N'%[0-9]%'
       OR @HO_TXT LIKE N'%[~!@#$%^&*()_+=`{}|:;"'',.<>/?\-]%'
       OR @TEN_TXT LIKE N'%[~!@#$%^&*()_+=`{}|:;"'',.<>/?\-]%'
       OR @HO_TXT LIKE N'%[[]%' OR @HO_TXT LIKE N'%]%'
       OR @TEN_TXT LIKE N'%[[]%' OR @TEN_TXT LIKE N'%]%'
       OR @SODTLL_TXT = N''
       OR LEN(@SODTLL_TXT) <> 10
       OR LEFT(@SODTLL_TXT, 1) <> N'0'
       OR @SODTLL_TXT LIKE N'%[^0-9]%'
        RETURN 1;

    SET @MAGV_CHUAN = LEFT(@MAGV_TXT + REPLICATE(N' ', 8), 8);
    SET @SODTLL_CHUAN = LEFT(@SODTLL_TXT + REPLICATE(N' ', 15), 15);

    IF EXISTS (SELECT 1 FROM dbo.GIAOVIEN WHERE RTRIM(MAGV) = RTRIM(@MAGV_CHUAN))
        RETURN 1;

    INSERT INTO dbo.GIAOVIEN(MAGV, HO, TEN, SODTLL, DIACHI)
    VALUES (@MAGV_CHUAN, @HO_TXT, @TEN_TXT, NULLIF(@SODTLL_CHUAN, REPLICATE(N' ', 15)), NULLIF(@DIACHI_TXT, N''));

    RETURN 0;
END
GO

