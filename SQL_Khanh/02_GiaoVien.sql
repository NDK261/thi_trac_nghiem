CREATE PROCEDURE dbo.SP_KHOIPHUC_GIAOVIEN
    @MAGV NVARCHAR(50)
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @MAGV_TXT NVARCHAR(50), @MAGV_CHUAN NCHAR(8);
    DECLARE @LOGIN_NAME SYSNAME, @SQL NVARCHAR(MAX);

    SET @MAGV_TXT = UPPER(LTRIM(RTRIM(ISNULL(@MAGV, N''))));

    IF @MAGV_TXT = N'' OR LEN(@MAGV_TXT) > 8
        RETURN 1;

    SET @MAGV_CHUAN = LEFT(@MAGV_TXT + REPLICATE(N' ', 8), 8);

    IF NOT EXISTS (
        SELECT 1
        FROM dbo.GIAOVIEN
        WHERE RTRIM(MAGV) = RTRIM(@MAGV_CHUAN)
          AND TRANGTHAI = 0
    )
        RETURN 1;

    SELECT @LOGIN_NAME = sp.name
    FROM sys.database_principals AS dp
    INNER JOIN master.sys.server_principals AS sp
        ON sp.sid = dp.sid
    WHERE dp.name = RTRIM(@MAGV_CHUAN)
      AND sp.type_desc = N'SQL_LOGIN';

    BEGIN TRY
        IF @LOGIN_NAME IS NOT NULL
        BEGIN
            SET @SQL = N'ALTER LOGIN ' + QUOTENAME(@LOGIN_NAME) + N' ENABLE;';
            EXEC (@SQL);
        END

        UPDATE dbo.GIAOVIEN
        SET TRANGTHAI = 1
        WHERE RTRIM(MAGV) = RTRIM(@MAGV_CHUAN);

        IF @LOGIN_NAME IS NULL
            RETURN 2;

        RETURN 0;
    END TRY
    BEGIN CATCH
        RETURN 1;
    END CATCH
END
