/*
================================================================================
SINH VIEN - QUAN LY THEO LOP + XOA CUNG / XOA MEM

Quy uoc:
    - TRANGTHAI = 1: sinh vien dang hoat dong.
    - TRANGTHAI = 0: sinh vien ngung hoat dong, khong dang nhap/thi nua.
    - Xoa sinh vien:
        + Chua co diem/bai thi tam: xoa cung khoi SINHVIEN.
        + Da co diem/bai thi tam: xoa mem bang TRANGTHAI = 0.
================================================================================
*/

IF COL_LENGTH(N'dbo.SINHVIEN', N'TRANGTHAI') IS NULL
BEGIN
    ALTER TABLE dbo.SINHVIEN
    ADD TRANGTHAI BIT NOT NULL
        CONSTRAINT DF_SINHVIEN_TRANGTHAI DEFAULT (1);
END
GO

