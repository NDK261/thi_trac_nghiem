IF EXISTS (SELECT 1 FROM dbo.LOP WHERE LEN(LTRIM(RTRIM(ISNULL(TENLOP, N'')))) > 40)
BEGIN
    RAISERROR(N'Du lieu LOP.TENLOP dang vuot 40 ky tu. Hay sua du lieu truoc khi chay script.', 16, 1);
END
GO

ALTER TABLE dbo.LOP ALTER COLUMN TENLOP NVARCHAR(40) NOT NULL;
GO

IF EXISTS (
    SELECT 1
    FROM dbo.LOP
    GROUP BY UPPER(LTRIM(RTRIM(TENLOP)))
    HAVING COUNT(*) > 1
)
BEGIN
    RAISERROR(N'Du lieu LOP.TENLOP dang bi trung. Hay sua du lieu truoc khi tao UNIQUE.', 16, 1);
END
GO

IF NOT EXISTS (
    SELECT 1
    FROM sys.key_constraints
    WHERE parent_object_id = OBJECT_ID(N'dbo.LOP')
      AND name = N'UQ_LOP_TENLOP'
)
BEGIN
    ALTER TABLE dbo.LOP ADD CONSTRAINT UQ_LOP_TENLOP UNIQUE (TENLOP);
END
