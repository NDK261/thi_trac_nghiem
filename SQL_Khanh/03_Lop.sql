CREATE PROCEDURE dbo.SP_SUA_LOP
    @MALOP NVARCHAR(50),
    @TENLOP NVARCHAR(100)
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @MALOP_TXT NVARCHAR(50), @TENLOP_TXT NVARCHAR(100), @MALOP_CHUAN NCHAR(15);
    SET @MALOP_TXT = UPPER(LTRIM(RTRIM(ISNULL(@MALOP, N''))));
    SET @TENLOP_TXT = LTRIM(RTRIM(ISNULL(@TENLOP, N'')));

    IF @MALOP_TXT = N'' OR @TENLOP_TXT = N''
       OR LEN(@MALOP_TXT) > 15 OR LEN(@TENLOP_TXT) > 40
       OR @MALOP_TXT COLLATE Latin1_General_BIN2 LIKE N'%[^A-Z0-9]%'
        RETURN 1;

    SET @MALOP_CHUAN = LEFT(@MALOP_TXT + REPLICATE(N' ', 15), 15);

    IF NOT EXISTS (SELECT 1 FROM dbo.LOP WHERE RTRIM(MALOP) = RTRIM(@MALOP_CHUAN))
        RETURN 1;

    IF EXISTS (
        SELECT 1
        FROM dbo.LOP
        WHERE UPPER(LTRIM(RTRIM(TENLOP))) = UPPER(@TENLOP_TXT)
          AND RTRIM(MALOP) <> RTRIM(@MALOP_CHUAN)
    )
        RETURN 1;

    UPDATE dbo.LOP
    SET TENLOP = @TENLOP_TXT
    WHERE RTRIM(MALOP) = RTRIM(@MALOP_CHUAN);

    RETURN 0;
END
GO

