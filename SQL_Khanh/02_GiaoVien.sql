CREATE PROCEDURE dbo.SP_XOA_GIAOVIEN
    @MAGV NVARCHAR(50)
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @MAGV_TXT NVARCHAR(50), @MAGV_CHUAN NCHAR(8);
    DECLARE @HAS_HISTORY BIT, @LOGIN_NAME SYSNAME, @SQL NVARCHAR(MAX);
    SET @MAGV_TXT = UPPER(LTRIM(RTRIM(ISNULL(@MAGV, N''))));

    IF @MAGV_TXT = N'' OR LEN(@MAGV_TXT) > 8
        RETURN 1;

    SET @MAGV_CHUAN = LEFT(@MAGV_TXT + REPLICATE(N' ', 8), 8);

    IF NOT EXISTS (
        SELECT 1
        FROM dbo.GIAOVIEN
        WHERE RTRIM(MAGV) = RTRIM(@MAGV_CHUAN)
          AND TRANGTHAI = 1
    )
        RETURN 1;

    SET @HAS_HISTORY = CASE
        WHEN EXISTS (SELECT 1 FROM dbo.BODE WHERE RTRIM(MAGV) = RTRIM(@MAGV_CHUAN))
          OR EXISTS (SELECT 1 FROM dbo.GIAOVIEN_DANGKY WHERE RTRIM(MAGV) = RTRIM(@MAGV_CHUAN))
        THEN 1 ELSE 0 END;

    SELECT @LOGIN_NAME = sp.name
    FROM sys.database_principals AS dp
    INNER JOIN master.sys.server_principals AS sp
        ON sp.sid = dp.sid
    WHERE dp.name = RTRIM(@MAGV_CHUAN)
      AND sp.type_desc = N'SQL_LOGIN';

    IF @LOGIN_NAME IS NOT NULL AND @LOGIN_NAME = ORIGINAL_LOGIN()
        RETURN 3;

    BEGIN TRY
        IF @LOGIN_NAME IS NOT NULL
        BEGIN
            SET @SQL = N'ALTER LOGIN ' + QUOTENAME(@LOGIN_NAME) + N' DISABLE;';
            EXEC (@SQL);
        END

        IF @HAS_HISTORY = 1
        BEGIN
            UPDATE dbo.GIAOVIEN
            SET TRANGTHAI = 0
            WHERE RTRIM(MAGV) = RTRIM(@MAGV_CHUAN);

            RETURN 2;
        END

        IF EXISTS (SELECT 1 FROM sys.database_principals WHERE name = RTRIM(@MAGV_CHUAN))
        BEGIN
            SET @SQL = N'DROP USER ' + QUOTENAME(RTRIM(@MAGV_CHUAN)) + N';';
            EXEC (@SQL);
        END

        IF @LOGIN_NAME IS NOT NULL
        BEGIN
            SET @SQL = N'DROP LOGIN ' + QUOTENAME(@LOGIN_NAME) + N';';
            EXEC (@SQL);
        END

        DELETE FROM dbo.GIAOVIEN
        WHERE RTRIM(MAGV) = RTRIM(@MAGV_CHUAN);

        RETURN 0;
    END TRY
    BEGIN CATCH
        RETURN 1;
    END CATCH
END
