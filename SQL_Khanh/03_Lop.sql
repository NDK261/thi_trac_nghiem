CREATE PROCEDURE dbo.SP_XOA_LOP
    @MALOP NVARCHAR(50)
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @MALOP_TXT NVARCHAR(50), @MALOP_CHUAN NCHAR(15);
    SET @MALOP_TXT = UPPER(LTRIM(RTRIM(ISNULL(@MALOP, N''))));

    IF @MALOP_TXT = N'' OR LEN(@MALOP_TXT) > 15
        RETURN 1;

    SET @MALOP_CHUAN = LEFT(@MALOP_TXT + REPLICATE(N' ', 15), 15);

    IF NOT EXISTS (SELECT 1 FROM dbo.LOP WHERE RTRIM(MALOP) = RTRIM(@MALOP_CHUAN))
        RETURN 1;

    IF EXISTS (SELECT 1 FROM dbo.SINHVIEN WHERE RTRIM(MALOP) = RTRIM(@MALOP_CHUAN))
       OR EXISTS (SELECT 1 FROM dbo.GIAOVIEN_DANGKY WHERE RTRIM(MALOP) = RTRIM(@MALOP_CHUAN))
        RETURN 1;

    DELETE FROM dbo.LOP
    WHERE RTRIM(MALOP) = RTRIM(@MALOP_CHUAN);

    RETURN 0;
END
GO
